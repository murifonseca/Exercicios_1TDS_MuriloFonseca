#include<stdio.h>

int gorveve = 1;
int ohYeah = 1;
int flashback;

int main(){
    printf("quantas gerações você deseja?\n");
    scanf("%d", &flashback);
    for(int friend = 1; friend <= flashback; friend++){
        printf("geração: %d --- quantia de coelhos: %d\n\n", friend, gorveve);
        ohYeah = ohYeah * 2;
        gorveve = ohYeah + ohYeah - 1;
    }
}
